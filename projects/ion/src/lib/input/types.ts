export interface IonInputProps {
  /*
   * disabled state of the input
   * @type {boolean}
   * @default false
   * @example
   * <input [disabled]="true" ionInput />
   * <input disabled ionInput />
   */
  disabled?: boolean;

  /*
   * key of the input
   * @type {string}
   * @default false
   * @example
   * <input [invalid]="true" ionInput />
   */
  invalid?: boolean;
}
